##### Cration des tables #####
DROP TABLE IF EXISTS Lecteur ;
CREATE TABLE Lecteur (
  numL int AUTO_INCREMENT NOT NULL,
  nomL VARCHAR(16),
  prenomL VARCHAR(16),
  adresseL VARCHAR(60),
  telephoneL VARCHAR(20),
  numParrain int,
  PRIMARY KEY (numL)
  ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Exemplaire ;
CREATE TABLE Exemplaire (
  idExemplaire int AUTO_INCREMENT NOT NULL,
  dateAchat DATE,
  ISBN VARCHAR(13) NOT NULL,
  PRIMARY KEY (idExemplaire) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Livre ;
CREATE TABLE Livre (
  ISBN VARCHAR(13) NOT NULL,
  DateParution DATE,
  SIRET INT NOT NULL,
  PRIMARY KEY (ISBN) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Ouvrage ;
CREATE TABLE Ouvrage (
  idOuvrage int AUTO_INCREMENT NOT NULL,
  TitreVO VARCHAR(200),
  TitreTraduit VARCHAR(200),
  AnneePremiereParution INT,
  IdType INT NOT NULL,
  IdStyle INT NOT NULL,
  PRIMARY KEY (idOuvrage) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Type ;
CREATE TABLE Type (
  IdType int AUTO_INCREMENT NOT NULL,
  nomT VARCHAR(10),
  descriptifT VARCHAR(60),
  PRIMARY KEY (IdType) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Auteur ;
CREATE TABLE Auteur (
  IdAuteur int AUTO_INCREMENT NOT NULL,
    nomPlume VARCHAR(20),
    PRIMARY KEY (IdAuteur) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Style ;
CREATE TABLE Style (
  IdStyle int AUTO_INCREMENT NOT NULL,
  NomS VARCHAR(20),
  DescriptifS VARCHAR(60),
  PRIMARY KEY (IdStyle) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Editeur ;
CREATE TABLE Editeur (
  SIRET int AUTO_INCREMENT NOT NULL,
  RaisonSociale VARCHAR(20),
  adresse VARCHAR(60),
  PRIMARY KEY (SIRET) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Emprunter ;
CREATE TABLE Emprunter (
  numL int AUTO_INCREMENT NOT NULL,
  idExemplaire INT NOT NULL,
  dateEmprunt DATE,
  dateRetour DATE,
  PRIMARY KEY (numL,  idExemplaire) ) ENGINE=InnoDB;

 DROP TABLE IF EXISTS Implementer ;
 CREATE TABLE Implementer (
   ISBN VARCHAR(13) NOT NULL,
   idOuvrage INT NOT NULL,
   PRIMARY KEY (ISBN,  idOuvrage) ) ENGINE=InnoDB;

DROP TABLE IF EXISTS Ecrire ;
CREATE TABLE Ecrire (
  idOuvrage int AUTO_INCREMENT NOT NULL,
  IdAuteur INT NOT NULL,
  PRIMARY KEY (idOuvrage,  IdAuteur) ) ENGINE=InnoDB;

